public class SwitchStatements {
    public static void main(String[] args) {
        int number = 3;

        switch(number){
            case 1 :
                System.out.println("Samosa");
                break;
            case 2 :
                System.out.println("Burger");
                break;
            case 3 :
                System.out.println("Mango Shake");
                break;
            default :
                System.out.println("Please!!!... enter correct number");
        }
    }
}
